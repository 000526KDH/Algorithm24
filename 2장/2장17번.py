def sub(n):
    if n <=1: return n
    return sub(n-1)+sub(n-2)
"""이 순환 알고리즘은 피보나치 수열을 계산하는 함수입니다. sub(n) 함수는 n이 1 이하일 때 n을 반환하고, 그렇지 않으면 sub(n-1) + sub(n-2)를 호출합니다. 이제 sub(3)을 호출했을 때 sub() 함수가 호출되는 전체 횟수를 계산해 보겠습니다.

sub(3)을 호출하면, 이는 sub(2) + sub(1)을 호출합니다.
sub(2)는 sub(1) + sub(0)을 호출합니다.
sub(1)은 기본 조건에 의해 바로 값을 반환하므로 여기서 1회 호출됩니다.
sub(0)도 기본 조건에 의해 바로 값을 반환하므로 여기서 1회 호출됩니다.
sub(1)은 기본 조건에 의해 바로 값을 반환하므로 여기서 1회 호출됩니다.
따라서 sub(3)을 호출할 때 총 호출 횟수는 다음과 같습니다:

sub(3) 자체 1회
sub(2)로 인해 1회
sub(2) 내부의 sub(1)과 sub(0)으로 인해 2회
sub(3)에서 바로 호출된 sub(1)로 인해 1회
총 합계: 1 + 1 + 2 + 1 = 5회 호출됩니다."""