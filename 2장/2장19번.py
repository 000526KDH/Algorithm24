def asterisk(i):
    if i>1:
        asterisk(i/2)
        asterisk(i/2)
    print("*",end='')

"""이 재귀 함수는 입력된 i가 1보다 클 때, i/2를 매개변수로 하여 자기 자신을 두 번 호출합니다. 그 후, 별표(*)를 출력합니다. 주어진 조건에서 asterisk(5)를 호출하면 다음과 같은 과정을 거칩니다:

asterisk(5) 호출:
i > 1이므로, asterisk(5/2) 즉, asterisk(2.5)를 두 번 호출합니다.
각 asterisk(2.5) 호출 후 *를 출력합니다.
asterisk(2.5) 호출 (첫 번째 호출):
i > 1이므로, asterisk(2.5/2) 즉, asterisk(1.25)를 두 번 호출합니다.
각 asterisk(1.25) 호출 후 *를 출력합니다.
asterisk(1.25) 호출:
i > 1이므로, asterisk(1.25/2) 즉, asterisk(0.625)를 두 번 호출합니다.
asterisk(0.625)는 i > 1 조건을 만족하지 않으므로, 재귀 호출 없이 *를 출력하고 종료됩니다.
두 번의 asterisk(0.625) 호출로 **가 출력됩니다.
asterisk(1.25)의 마지막 부분에서 *가 추가로 출력되어 총 ***가 됩니다.
첫 번째 asterisk(2.5) 호출이 종료된 후, 두 번째 asterisk(2.5) 호출이 동일한 과정을 거쳐 ***를 출력합니다.
마지막으로 asterisk(5)의 마지막 부분에서 *가 추가로 출력되어, 총 *******가 됩니다.
따라서 asterisk(5)를 호출했을 때 총 7개의 별표(*)가 출력됩니다."""